class Smallestof5numbers 
{
	public static void main(String[] args) 
	{
		int a=10;
		int b=20;
		int c=30;
		int d=40;
		int e=50;
		String res=((a<b)&&(a<c)&&(a<d)&&(a<e))?"a is smallest":((b<a)&&(b<c)&&(b<d)&&(b<e))?"b is smallest":((c<a)&&(c<b)&&(c<d)&&(c<e))?"c is smallest":((d<a)&&(d<b)&&(d<c)&&(d<e))?"d is smallest":"e is smallest";
		System.out.println(res);
	}
}
